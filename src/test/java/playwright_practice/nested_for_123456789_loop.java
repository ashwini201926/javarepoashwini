package practice;

public class nested_for_123456789_loop {

	public static void main(String[] args) {
		// Nested for loop to print 123 456 789 in each row
		
		int print=1;
		for(int i=1;i<=3;i++) {
			for(int j=1;j<=3;j++) {
				System.out.print(print);	
				print++;
			}
				
			System.out.println();
		}
		

	}

}
