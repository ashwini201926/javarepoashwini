package practice;

public class hw_asterisk_3_3 {

	public static void main(String[] args) {
		// nested for loop to generate output * (***,***,***) each row

		char a='*';
		for(int i=1;i<=3;i++) {
			for(int j=1;j<=3;j++) {
				System.out.print(a);
				
			}
			System.out.println();
		}
		
		
		
	}

}
